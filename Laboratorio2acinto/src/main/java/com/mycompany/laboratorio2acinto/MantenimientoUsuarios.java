    private void jButton_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AgregarActionPerformed
    String idNuevo = jText_Usuario.getText();
    boolean existe = false;

    // 1. Validar campos vacíos
    if (idNuevo.isEmpty() || jText_Nombre.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar los campos obligatorios");
        return; // Salimos del método para no seguir
    }

    // 2. RECORRIDO DE VALIDACIÓN: Buscamos en toda la tabla
    for (int i = 0; i < jTable_Usuarios.getRowCount(); i++) {
        // Comparamos el ID nuevo con el que ya está en la fila 'i', columna 0
        if (jTable_Usuarios.getValueAt(i, 0).toString().equals(idNuevo)) {
            existe = true;
            break; // Si lo encontramos, dejamos de buscar
        }
    }

    // 3. Decisión final
    if (existe) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: El usuario con ID " + idNuevo + " ya existe.");
    } else {
        // Si no existe, lo agregamos normalmente
        String[] datos = {idNuevo, jText_Nombre.getText(), jText_Puesto.getText(), "ACTIVO","12345"};
        //modelo.addRow(datos);
        DatosGlobales.modeloGlobal.addRow(datos);
        limpiarCampos();
        javax.swing.JOptionPane.showMessageDialog(this, "Usuario registrado con éxito");
    }
    }//GEN-LAST:event_jButton_AgregarActionPerformed

