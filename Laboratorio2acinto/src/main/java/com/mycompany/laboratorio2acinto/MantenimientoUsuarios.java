    private void jButton_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ModificarActionPerformed
int filaSeleccionada = jTable_Usuarios.getSelectedRow();
    
    if (filaSeleccionada != -1) {
        // 1. Obtenemos los nuevos datos de los campos
        String nuevoNombre = jText_Nombre.getText();
        String nuevoPuesto = jText_Puesto.getText();
        
        // CORRECCIÓN AQUÍ: Usamos get y convertimos a String
        String nuevoEstado = cbEstado.getSelectedItem().toString();
        
        // 2. Validamos que no estén vacíos
        if (nuevoNombre.isEmpty() || nuevoPuesto.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Los campos Nombre y Puesto no pueden estar vacíos");
            return;
        }

        // 3. Actualizamos el MODELO GLOBAL
        DatosGlobales.modeloGlobal.setValueAt(nuevoNombre, filaSeleccionada, 1);
        DatosGlobales.modeloGlobal.setValueAt(nuevoPuesto, filaSeleccionada, 2);
        DatosGlobales.modeloGlobal.setValueAt(nuevoEstado, filaSeleccionada, 3);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Datos actualizados correctamente");
        limpiarCampos();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione la fila que desea modificar");
    }
    }//GEN-LAST:event_jButton_ModificarActionPerformed

