    private void jButton_InactivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_InactivarActionPerformed
    int filaSeleccionada = jTable_Usuarios.getSelectedRow();
    
    if (filaSeleccionada != -1) {
        // 1. Obtenemos el estado actual de la columna 3
        String estadoActual = DatosGlobales.modeloGlobal.getValueAt(filaSeleccionada, 3).toString();
        
        // 2. Verificamos si ya es "INACTIVO"
        if (estadoActual.equalsIgnoreCase("INACTIVO")) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "El usuario ya se encuentra en estado INACTIVO.", 
                "Aviso", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return; // Salimos del método para no hacer nada más
        }

        // 3. Si no estaba inactivo, procedemos a cambiarlo
        DatosGlobales.modeloGlobal.setValueAt("INACTIVO", filaSeleccionada, 3);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Usuario inactivado con éxito");
        limpiarCampos();
        
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione un usuario de la tabla");
    }
    }//GEN-LAST:event_jButton_InactivarActionPerformed

