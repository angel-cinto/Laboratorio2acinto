    class PasswordRenderer extends DefaultTableCellRenderer {
    @Override
    protected void setValue(Object value) {
        if (value == null) {
            setText("");
        } else {
            // Reemplazamos cada letra por un asterisco
            String str = value.toString();
            setText("*".repeat(str.length())); 
        }
    }
}
