    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
    int fila = jTable_UsuariosClave.getSelectedRow();
    
    // 1. Verificar selección
    if (fila == -1) {
        jLabel_Mensaje.setText("Seleccione un usuario de la tabla.");
        jLabel_Mensaje.setForeground(java.awt.Color.RED);
        return;
    }

    // 2. Obtener textos de los JPasswordField
    String pass1 = new String(jPass_NewPass.getPassword());
    String pass2 = new String(jPass_NewPassConf.getPassword());

    // 3. Validar que coincidan
    if (!pass1.equals(pass2)) {
        jLabel_Mensaje.setText("Las contraseñas no coinciden.");
        jLabel_Mensaje.setForeground(java.awt.Color.RED);
        return;
    }

    // 4. Validar complejidad y longitud
    if (!validarComplejidad(pass1)) {
        jLabel_Mensaje.setText("Debe tener +12 caracteres, Mayús, Minús y Núm.");
        jLabel_Mensaje.setForeground(java.awt.Color.RED);
        return;
    }

    // 5. Si todo está OK, actualizamos el modelo global
    // Recuerda: la columna 4 es donde guardamos el password
    DatosGlobales.modeloGlobal.setValueAt(pass1, fila, 4);
    
    // Limpieza y éxito
    jLabel_Mensaje.setText("¡Contraseña actualizada con éxito!");
    jLabel_Mensaje.setForeground(new java.awt.Color(0, 153, 0)); // Color verde
    jPass_NewPass.setText("");
    jPass_NewPassConf.setText("");
    }//GEN-LAST:event_jButton_UpdateActionPerformed

