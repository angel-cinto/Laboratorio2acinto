    private boolean validarComplejidad(String pass) {
    // Mínimo 12 caracteres, una mayúscula, una minúscula y un número
    String regex = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z]).{12,}$";
    return pass.matches(regex);
}
    class PasswordRenderer extends DefaultTableCellRenderer {
    @Override
    protected void setValue(Object value) {
        if (value == null) {
            setText("");
        } else {
            // Reemplazamos cada letra por un asterisco
            String str = value.toString();
            setText("*".repeat(str.length())); 
        }
    }
}
